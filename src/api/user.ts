import { fetch } from "../utils";

interface User {
  id: number;
  name: string;
  username: string;
  email: string;
  address: {
    street: string;
    suite: string;
    city: string;
    zipcode: string;
    geo: {
      lat: string;
      lng: string;
    };
  };
  phone: string;
  website: string;
  company: {
    name: string;
    catchPhrase: string;
    bs: string;
  };
}

const resource = "users";

export const getUsers = fetch(
  `${process.env.REACT_APP_JSON_PLACEHOLDER_API_URL}${resource}`
);
